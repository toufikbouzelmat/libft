#include "libft.h"

int	ft_isalnum(int c)
{
	if (((c >= 97 && c <= 122)
			|| (c >= 65 && c <= 90)
			|| (c >= 48 && c <= 57)))
	{
		return (c);
	}
	return (0);
}
